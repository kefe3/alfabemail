      <li>Hesap bilgilerinizin gizliliğinden siz sorumlusunuz</li>
      <li>QR kod girişleri yalnızca hesap sahibine ait cihazlarda kullanılmalıdır</li>
      <li>Hesabınızı yetkisiz kullanıma karşı korumakla yükümlüsünüz</li>
