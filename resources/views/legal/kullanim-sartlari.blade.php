      <li>E-posta iletişimi yalnızca onaylı kişilerle (sınıf arkadaşları, öğretmenler) sınırlıdır</li>
      <li>Öğrenci hesaplarından dışarıya dosya gönderimi kısıtlıdır</li>
      <li>Yaka kartı ve QR kod yalnızca ilgili öğrenci tarafından kullanılabilir</li>
    </ul>

